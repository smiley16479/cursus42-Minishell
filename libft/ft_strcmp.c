#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	unsigned int i;

	i = 0;
	while (((s1[i]) == s2[i]) && (s1[i] || s2[i]))
	{
		i++;
	}
	return ((unsigned)(s1[i]) - (unsigned)(s2[i]));
}
